    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = barangTable.getSelectedRow();
        // Ambil kode barang dari tabel sebagai string
        String selectedKodeBarang = barangTable.getValueAt(selectedRow, 0).toString();

        // Mengambil data barang dari database
        Barang barang = controller.getBarangByKodeBarang(selectedKodeBarang);

        if (barang != null) {
            String kode = barangTable.getValueAt(selectedRow, 0).toString();
            String nama = barangTable.getValueAt(selectedRow, 1).toString();
            int stok = Integer.parseInt(barangTable.getValueAt(selectedRow, 2).toString());
            int harga = Integer.parseInt(barangTable.getValueAt(selectedRow, 3).toString());

            Kasir.kodeBarangTextField.setText(kode);
            Kasir.namaBarangTextField.setText(nama);
            Kasir.hargaTextField.setText(String.valueOf(harga));
            Kasir.qtyTextField.setEditable(true);
            Kasir.qtyTextField.requestFocus();
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_okButtonActionPerformed

