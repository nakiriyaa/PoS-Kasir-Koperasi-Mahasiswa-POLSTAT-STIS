        int selectedRow = barangTable.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ada", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

